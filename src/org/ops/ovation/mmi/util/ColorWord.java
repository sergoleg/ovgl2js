	/**
	 * Convert int to RGB and to Hexadecimal
	 */
	public static String toHex(Integer i) {
		Color hC = new Color(i);
		String hex = Integer.toHexString(hC.getRGB() & 0xffffff);
		while (hex.length() < 6) {
			hex = hex + "0";
		}
		//hex = "\"#" + hex + "\"";
		hex = "#" + hex;
		return hex;
	}

